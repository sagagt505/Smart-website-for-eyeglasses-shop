  </div>
  <p>Glasses, also known as eyeglasses or spectacles, are devices consisting of glass or hard plastic lenses mounted in a frame that holds them in front of a person's eyes, typically using a bridge over the nose and arms which rest over the ears. Glasses are typically used for vision correction, such as with reading glasses and glasses used for nearsightedness. Safety glasses provide eye protection against flying debris for construction workers or lab technicians; these glasses may have protection for the sides of the eyes as well as in the lenses. Some types of safety glasses are used to protect against visible and near-visible light or radiation. Glasses are worn for eye protection in some sports, such as squash. Glasses wearers may use a strap to prevent the glasses from falling off during movement or sports. Wearers of glasses that are used only part of the time may have the glasses attached to a cord that goes around their neck, to prevent the loss of the glasses.</p>

  <p> Sunglasses allow better vision in bright daylight, and may protect one's eyes against damage from high levels of ultraviolet light. Typical sunglasses are darkened for protection against bright light or glare; some specialized glasses are clear in dark or indoor conditions, but turn into sunglasses when in bright light. Most sunglasses do not have corrective power in the lenses; however, special prescription sunglasses can be ordered. Specialized glasses may be used for viewing specific visual information (such as stereoscopy) or 3D glasses for viewing three-dimensional movies. Sometimes glasses with no corrective power in the lenses are worn simply for aesthetic or fashion purposes. Even with glasses used for vision correction, a wide range of designs are available for fashion purposes, using plastic, wire, and other materials.</p>

  <p>People are more likely to need glasses the older they get with 93% of people between the age of 65-75 wearing corrective lenses.</p>         
